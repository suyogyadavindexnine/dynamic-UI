let inspiration = document.querySelector("#inspiration");
let inspirationmenu = document.querySelector("#inspirationmenu");
inspiration.onmouseover = () => {
    inspirationmenu.style.visibility = "visible";
}
inspirationmenu.onmouseover = () => {
    inspirationmenu.style.visibility = "visible";
}
inspirationmenu.onmouseout = () => {
    inspirationmenu.style.visibility = "hidden";
}

